package com.tts;

public class Refurbished {

    private String size;
    private String color;
    private String type;

    public Refurbished(String initSize, String initColor, String initType) {
        size = initSize;
        color = initColor;
        type = initType;
    }

    public String getSize() {
        return size;
    }

    public String getColor() {
        return color;
    }

    public String getType() {
        return type;

    }

}
